function getSoundDescription(sound, description, normalize) {
  if (typeof description == 'undefined') {
    description = false;
  }
  if (typeof normalize == 'undefined') {
    normalize = false;
  }

  if (normalize) {
    var normalized_sound = normalize_ipa(sound);
  }
  else {
    var normalized_sound = sound;
  }

  if (!normalized_sound) {
      return '';
  }
  else {
    var data = SOUNDS[normalized_sound];
  }

  if (typeof data == 'undefined') {
    return '';
  }

  if (!description) {
    return data;
  }
  else {
    return data[['type','manner','place','misc1', 'misc2'].indexOf(description)];
  }
}

